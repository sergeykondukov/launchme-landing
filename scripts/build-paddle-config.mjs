/**
 * Writes a small browser config for the /paddle checkout page.
 *
 * Why this exists:
 * - This repo is a static site, so browser code cannot read server env vars directly.
 * - We safely inject public Paddle checkout config at build time.
 * - Secrets stay in host/CI environment and do not need to be hard-coded in HTML.
 *
 * Usage:
 *   pnpm run build:paddle
 */
import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

function rootDir() {
  return join(dirname(fileURLToPath(import.meta.url)), '..')
}

const root = rootDir()

// Load local .env if present. CI can provide env vars directly.
try {
  const dotenv = await import('dotenv')
  dotenv.config({ path: join(root, '.env') })
} catch {
  // dotenv can be unavailable before dependencies are installed
}

const outDir = join(root, 'js')
const outFile = join(outDir, 'paddle-config.js')

if (!existsSync(outDir)) {
  mkdirSync(outDir, { recursive: true })
}

const clientToken = process.env.PADDLE_CLIENT_TOKEN ?? ''
const envRaw = (process.env.PADDLE_ENV ?? 'production').toLowerCase()
const env = envRaw === 'sandbox' ? 'sandbox' : 'production'

const content = `// Generated by scripts/build-paddle-config.mjs
// Do not edit manually. Rebuild with: pnpm run build:paddle
window.PADDLE_CONFIG = {
  clientToken: ${JSON.stringify(clientToken)},
  env: ${JSON.stringify(env)}
};
`

writeFileSync(outFile, content, 'utf8')
console.log('Wrote', outFile)
